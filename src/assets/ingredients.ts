import { DIETS_OCCASIONS } from './recipes';

export enum MEASUREMENT {
  DL = 'DL',
  GRAM = 'GRAM',
  KG = 'KG',
  KRM = 'KRM',
  L = 'L',
  MSK = 'MSK',
  ST = 'ST',
  TSK = 'TSK',
  PORTIONS = 'PORTIONS',
  BATCH = 'BATCH',
}

export interface Ingredient {
  ingredientAlternatives: Array<string>;
  amount?: string;
  measurement?: MEASUREMENT;
  optional?: boolean;
  gram?: number;
  link?: string; // To existing recipes
  exclude_for_diets?: Array<DIETS_OCCASIONS>;
}

export function toIngredient(
  ingredientAlternatives: Array<string>,
  amount?: string,
  measurement?: MEASUREMENT,
  optional?: boolean,
  gram?: number,
  link?: string,
  exclude_for_diets?: Array<DIETS_OCCASIONS>
): Ingredient {
  return {
    amount,
    measurement,
    ingredientAlternatives,
    optional,
    gram,
    link,
    exclude_for_diets,
  };
}
