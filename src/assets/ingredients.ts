export enum SECTION {
  SERVING = 'SERVING',
  JAM = 'JAM',
  DOUGH = 'DOUGH',
  DRESSING = 'DRESSING',
  SAUCE = 'SAUCE',
}

export interface Ingredient {
  amount: string;
  measurement: MEASUREMENT;
  ingredientAlternatives: Array<string>;
  section?: SECTION;
  optional: boolean;
}

export function toIngredient(
  amount: string,
  measurement: MEASUREMENT,
  ingredientAlternatives: Array<string>,
  section?: SECTION,
  optional: boolean = false
): Ingredient {
  return {
    amount,
    measurement,
    ingredientAlternatives,
    section,
    optional,
  };
}
